
// CPP program to illustrate
// std::to_string
#include <bits/stdc++.h>

// Driver code
int main()
{

    // Converting float to string
    std::string str1 = std::to_string(12.10);

    // Converting integer to string
    std::string str2 = std::to_string(9999);

    // Printing the strings
    std::cout << str1 << '\n';
    std::cout << str2 << '\n';
    return 0;
}
